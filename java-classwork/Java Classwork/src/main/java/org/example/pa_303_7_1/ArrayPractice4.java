package org.example.pa_303_7_1;

public class ArrayPractice4 {
    public static void main(String[] args) {

        int[] elements = {1, 2, 3, 4, 5, 6};

        System.out.println("The value at the first index is " + elements[1]);
        System.out.println("The value at the last index is " + elements[elements.length-1]);
        System.out.println("The value at the length index is " + elements[elements.length]);
    }
}
